/** The preload image options */
export interface IPreload {
  /** Enables gif support */
  gif: boolean;
  /** The image height */
  height?: number;
  /** The image name */
  name?: string;
  /** Replace the color with the particle color */
  replaceColor?: boolean;
  /** The image source */
  src: string;
  /** The image width */
  width?: number;
}
